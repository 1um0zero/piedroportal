// Generates src/lib/dataverse-option-codes.ts from docs/dataverse-option-sets.json.
// Transports every Dataverse choice column + numeric code into the app so the
// ERP cross-reference (codes 9795800xx) is available in code, not just in a CSV.
//
//   node scripts/dataverse-dump-optionsets.mjs   # refresh the dump first
//   node scripts/gen-option-codes.mjs
//
// Keyed by the BASE Dataverse field name (the sided _lf/_rf pair is collapsed,
// since both carry the same global option set). Base names match the
// `dataverseKey` used in additions-config.ts.

import { readFileSync, writeFileSync } from 'fs'
import { resolve } from 'path'

const dump = JSON.parse(readFileSync(resolve(process.cwd(), 'docs/dataverse-option-sets.json'), 'utf8'))

const byBase = {}
for (const [field, meta] of Object.entries(dump.fields)) {
  const base = field.endsWith('lf') ? field.slice(0, -2)
            : field.endsWith('rf') ? null            // skip the right-foot twin
            : field
  if (base === null) continue
  // First writer wins; lf and the non-sided variant are equivalent.
  if (!byBase[base]) byBase[base] = { optionSet: meta.optionSet, options: meta.options }
}

const sorted = Object.keys(byBase).sort()
const body = sorted.map(base => {
  const { optionSet, options } = byBase[base]
  const opts = options
    .sort((a, b) => a.value - b.value)
    .map(o => `    { code: ${o.value}, label: ${JSON.stringify(o.label)} },`)
    .join('\n')
  return `  '${base}': {\n    optionSet: ${JSON.stringify(optionSet)},\n    options: [\n${opts}\n    ],\n  },`
}).join('\n')

const out = `// AUTO-GENERATED by scripts/gen-option-codes.mjs — DO NOT EDIT BY HAND.
// Source: docs/dataverse-option-sets.json (entity ${dump.entity}).
// Every Dataverse choice column with its numeric option-set codes, so the ERP
// integration (and any validation) can cross-reference code <-> label without a
// live Dataverse call. Re-generate after running scripts/dataverse-dump-optionsets.mjs.

export interface DataverseOption { code: number; label: string }
export interface DataverseOptionSet { optionSet: string | null; options: DataverseOption[] }

// Keyed by the base Dataverse field name (matches \`dataverseKey\` in additions-config.ts).
export const DATAVERSE_OPTION_CODES: Record<string, DataverseOptionSet> = {
${body}
}

/** Look up the numeric Dataverse code for a stored label on a given base field. */
export function codeForLabel(dataverseKey: string, label: string): number | null {
  const set = DATAVERSE_OPTION_CODES[dataverseKey]
  if (!set) return null
  return set.options.find(o => o.label === label)?.code ?? null
}

/** Look up the label for a numeric Dataverse code on a given base field. */
export function labelForCode(dataverseKey: string, code: number): string | null {
  const set = DATAVERSE_OPTION_CODES[dataverseKey]
  if (!set) return null
  return set.options.find(o => o.code === code)?.label ?? null
}
`

writeFileSync(resolve(process.cwd(), 'src/lib/dataverse-option-codes.ts'), out)
console.log(`Wrote src/lib/dataverse-option-codes.ts — ${sorted.length} fields.`)
